CREATE TABLE historicalQuotes(ticker VARCHAR(45),
					date DATE,
					open REAL,
					high REAL,
					low REAL,
					close REAL,
					volume INTEGER,
					adjClose REAL,
					PRIMARY KEY (ticker,date));

